const selectedDayArr = [];
const selectedDay = day => {
  selectedDayArr.length === 2
    ? selectedDayArr.splice(0, 2, day)
    : selectedDayArr.push(day);
    makeMarkedDatesJson(selectedDayArr)
  };

const makeMarkedDatesJson = selectedDayArr => {
  let selectInfo = {};
  if (selectedDayArr.length !== 0) {
    let startDayString = selectedDayArr[0].dateString;
    let endDayString = selectedDayArr[1] ? selectedDayArr[1].dateString : null;
    selectInfo[startDayString] = { startingDay: true, color: "green" };
    selectInfo[endDayString] = {
      selected: true,
      endingDay: true,
      color: "green"
    };
    console.log("selectInfo",selectInfo)
    return selectInfo;
  } else {
    return null
  }
};

